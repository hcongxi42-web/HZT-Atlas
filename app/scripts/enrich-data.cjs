// ========== 股票描述生成函数 ==========
function generateStockDesc(name, code, tag, nodeName) {
  if (code === '未上市') {
    return `${name}是${nodeName}领域的重要企业，${tag}，目前尚未在A股上市。`;
  }
  // 简化tag中的特殊字符
  let cleanTag = tag
    .replace(/specialist/g, '专业供应商')
    .replace(/specialist/g, ' specialist')
    .replace(/市占率/g, '市场占有率')
    .replace(/毛利率/g, '毛利率水平')
    .replace(/ROE/g, '净资产收益率')
    .replace(/IRR/g, '内部收益率');
  
  return `${name}（${code}）是${nodeName}领域的代表性上市公司，${cleanTag}，在产业链中占据重要位置。`;
}

// ========== 处理函数 ==========
function processFile(filePath, industry) {
  let content = fs.readFileSync(filePath, 'utf8');
  const map = nodePlainDescMap[industry];
  if (!map) {
    console.warn(`No description map for ${industry}`);
    return;
  }

  // 1. 为节点注入 plainLanguageDescription
  const nodeRegex = /\{ id: (\d+), name: "([^"]+)", level: "([^"]+)", description: "([^"]*)"/g;
  const nodeReplacements = [];
  let match;
  while ((match = nodeRegex.exec(content)) !== null) {
    const [full, idStr] = match;
    const id = Number(idStr);
    const plainDesc = map[id];
    // 检查该位置附近是否已有 plainLanguageDescription，避免重复注入
    const surrounding = content.substring(match.index, match.index + full.length + 60);
    if (plainDesc && !surrounding.includes('plainLanguageDescription')) {
      nodeReplacements.push({ index: match.index, old: full, new: `${full}, plainLanguageDescription: "${plainDesc}"` });
    }
  }
  // 从后往前替换
  for (let i = nodeReplacements.length - 1; i >= 0; i--) {
    const r = nodeReplacements[i];
    content = content.slice(0, r.index) + r.new + content.slice(r.index + r.old.length);
  }
  console.log(`[${industry}] 注入 ${nodeReplacements.length} 个节点大白话`);

  // 2. 为股票注入 description
  // 先找到所有节点定义（含stocks数组），提取 nodeName
  const stockRegex = /\{ id: (\d+), name: "([^"]+)", code: "([^"]+)", tag: "([^"]*)" \}/g;
  const stockReplacements = [];
  while ((match = stockRegex.exec(content)) !== null) {
    const [full, idStr, name, code, tag] = match;
    const id = Number(idStr);
    // 向上查找最近的 node name
    const before = content.substring(0, match.index);
    const allNodeMatches = [...before.matchAll(/\{ id: \d+, name: "([^"]+)"/g)];
    let nodeName = '';
    if (allNodeMatches.length > 0) {
      nodeName = allNodeMatches[allNodeMatches.length - 1][1];
    }
    const desc = generateStockDesc(name, code, tag, nodeName || '相关');
    // 检查是否已有 description
    const surrounding = content.substring(match.index, match.index + full.length + 20);
    if (!surrounding.includes('description')) {
      stockReplacements.push({ index: match.index, old: full, new: `{ id: ${id}, name: "${name}", code: "${code}", tag: "${tag}", description: "${desc}" }` });
    }
  }
  for (let i = stockReplacements.length - 1; i >= 0; i--) {
    const r = stockReplacements[i];
    content = content.slice(0, r.index) + r.new + content.slice(r.index + r.old.length);
  }
  console.log(`[${industry}] 注入 ${stockReplacements.length} 个股票描述`);

  fs.writeFileSync(filePath, content);
}

